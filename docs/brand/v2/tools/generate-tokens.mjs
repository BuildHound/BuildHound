#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

const toolDirectory = path.dirname(fileURLToPath(import.meta.url));
const sourcePath = path.resolve(toolDirectory, "../assets/tokens/tokens.json");
const outputPath = path.resolve(toolDirectory, "../assets/tokens/tokens.css");
const checkOnly = process.argv.includes("--check");

const tokens = JSON.parse(fs.readFileSync(sourcePath, "utf8"));
const foundationNames = [
  "canvas",
  "surface",
  "surface-subtle",
  "surface-raised",
  "grid",
  "text",
  "text-secondary",
  "text-muted",
  "border",
  "control-border",
  "brand",
  "brand-text",
  "brand-soft",
  "focus"
];
const semanticNames = [
  "success",
  "failure",
  "info",
  "flaky",
  "running",
  "warning",
  "neutral"
];
const semanticRoles = ["fg", "bg", "solid"];

function requireValue(value, label) {
  if (typeof value !== "string" || value.length === 0) {
    throw new Error(`Missing token value: ${label}`);
  }
  return value;
}

function themeLines(themeName) {
  const foundation = tokens.foundation?.[themeName];
  const semantic = tokens.semantic?.[themeName];
  if (!foundation || !semantic) {
    throw new Error(`Missing ${themeName} token theme`);
  }

  const lines = foundationNames.map((name) =>
    `  --bh-${name}: ${requireValue(foundation[name], `${themeName}.foundation.${name}`)};`
  );
  lines.push("");
  for (const state of semanticNames) {
    for (const role of semanticRoles) {
      lines.push(
        `  --bh-${state}-${role}: ${requireValue(semantic[state]?.[role], `${themeName}.semantic.${state}.${role}`)};`
      );
    }
  }
  return lines;
}

function sharedLines() {
  return [
    `  --bh-font-display: ${requireValue(tokens.typography?.["font-display"], "typography.font-display")};`,
    `  --bh-font-ui: ${requireValue(tokens.typography?.["font-ui"], "typography.font-ui")};`,
    `  --bh-font-mono: ${requireValue(tokens.typography?.["font-mono"], "typography.font-mono")};`,
    "",
    `  --bh-radius-sm: ${requireValue(tokens.radius?.sm, "radius.sm")};`,
    `  --bh-radius-md: ${requireValue(tokens.radius?.md, "radius.md")};`,
    `  --bh-radius-lg: ${requireValue(tokens.radius?.lg, "radius.lg")};`,
    "",
    ...Object.keys(tokens.spacing ?? {}).map(
      (key) => `  --bh-space-${key}: ${requireValue(tokens.spacing[key], `spacing.${key}`)};`
    )
  ];
}

function block(selector, themeName, colorScheme, includeShared = false) {
  const lines = [`${selector} {`, `  color-scheme: ${colorScheme};`, ...themeLines(themeName)];
  if (includeShared) {
    lines.push("", ...sharedLines());
  }
  lines.push("}");
  return lines.join("\n");
}

const generated = [
  "/*",
  " * Generated from tokens.json by ../../tools/generate-tokens.mjs.",
  " * Do not edit this file directly; update tokens.json and regenerate it.",
  " */",
  "",
  block(":root", "light", "light dark", true),
  "",
  block(':root[data-theme="light"]', "light", "light"),
  "",
  "@media (prefers-color-scheme: dark) {",
  block(':root:not([data-theme="light"])', "dark", "dark")
    .split("\n")
    .map((line) => (line.length > 0 ? `  ${line}` : ""))
    .join("\n"),
  "}",
  "",
  block(':root[data-theme="dark"]', "dark", "dark"),
  "",
  "@media print {",
  block(":root,\n:root:not([data-theme]),\n:root[data-theme]", "light", "light")
    .split("\n")
    .map((line) => (line.length > 0 ? `  ${line}` : ""))
    .join("\n"),
  "}",
  ""
].join("\n");

if (checkOnly) {
  const current = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, "utf8") : "";
  if (current !== generated) {
    console.error("tokens.css is missing or stale; run generate-tokens.mjs");
    process.exitCode = 1;
  } else {
    console.log("tokens.css is up to date");
  }
} else {
  fs.writeFileSync(outputPath, generated);
  console.log(`Wrote ${path.relative(process.cwd(), outputPath)}`);
}
